package org.example;

/* Um programa que imprima a ficha de um aluno com nome, idade, turma e instituição de ensino
* */

public class A001Q05 {
    public static void main(String[] args) {
        String nome = "Aluno Exemplar";
        int idade = 15;
        String turma = "Turma Exemplar";
        String instituicao = "IFBA";

        System.out.println("___________________________");
        System.out.println("Ficha do aluno: " + nome);
        System.out.println("Idade: " + idade);
        System.out.println("Turma: " + turma);
        System.out.println("Instituicao: " + instituicao);
        System.out.println("___________________________");

    }
}
